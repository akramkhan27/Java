// OOPS -> classes and Objects
// There are 4 pillars of OOPS
// 1.Encapsulation ->
// Encapsulation in Java is the concept of wrapping the variables and methods into a single unit a class, 
// It helps in hiding the internal details of how an object works and
// protects its data from unauthorized access by restricting it through access modifiers private.
// The only way to interact with those variable is through public methods getter and setter.

// 2.Inheritance -> 
// In Inheritance, where a new class, known as a subclass or child class, 
// inherits the properties and behaviors of an existing class, called the superclass or parent class. 
// This allows for code reusability.
// In Java, inheritance is achieved using the extends keyword.

// Polymorphism ->
// Polymorphism in Java allows objects to be treated as instances of their parent class or 
// (object type is child class and reference type is parent class)
// rather than their actual class.
// There are two types:

// Compile-time Polymorphism (Method Overloading): 
// Multiple methods with the same name but different parameters within the same class.

// Runtime Polymorphism (Method Overriding): 
// A subclass provides a specific implementation of a method that is already defined in its superclass.


// Abstraction ->
// Abstraction in Java means hiding the unnecessary details and showing only the important things. 
// Think of it like when you're driving a car — you don't need to know how the engine works to drive it. 
// You just use the steering, brake, and accelerator. 
// In Java, we use abstraction to hide complex code from the user and show only what’s needed.

// In Java, abstraction is done using abstract classes and interfaces:

// Abstract class: A class that cannot be used directly to create objects. 
// It can have both abstract (no body) and regular methods.

// Interface: where only method names are given, 
// and the actual body is written in the classes that implement the interface.

